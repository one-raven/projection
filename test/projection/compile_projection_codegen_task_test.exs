  end

  test "projection.codegen emits typed list bindings from schema items option" do
    module_name = :"TypedListScreen#{System.unique_integer([:positive])}"
    module = Module.concat([Projection, module_name])

    source = """
    defmodule #{inspect(module)} do
      use ProjectionUI, :screen

      schema do
        field :tiles, :list, items: :integer, default: [1, 2, 3]
        field :ratios, :list, items: :float, default: [1.0, 0.5]
        field :flags, :list, items: :bool, default: [true, false]
        field :labels, :list, default: ["a", "b"]
      end

      @impl true
      def render(assigns), do: assigns
    end
    """

    Code.compile_string(source)

    cleanup = setup_codegen_env(router_module: :delete, screen_modules: [module])
    on_exit(cleanup)

    capture_io(fn ->
      Mix.Tasks.Projection.Codegen.run([])
    end)

    screen_name =
      module
      |> Module.split()
      |> List.last()
      |> Macro.underscore()

    state_slint = File.read!("slint/ui_host/src/generated/#{screen_name}_state.slint")
    refute state_slint =~ "import {", "no id_table structs => no types import in state file"

    types_only =
      File.read!("slint/ui_host/src/generated/#{screen_name}_types.slint")

    assert types_only =~ "generated by mix projection.codegen"
    refute types_only =~ "export struct"

    assert state_slint =~ "in property <[int]> tiles: [1, 2, 3];"
    assert state_slint =~ ~r/in property <\[float\]> ratios: \[(1|1\.0), 0\.5\];/
    assert state_slint =~ "in property <[bool]> flags: [true, false];"
    assert state_slint =~ "in property <[string]> labels: [\"a\", \"b\"];"

    screen_rs = File.read!("slint/ui_host/src/generated/#{screen_name}.rs")

    assert screen_rs =~
             "fn parse_integer_list(value: &Value, path: &str) -> Result<Vec<i64>, String>"

    assert screen_rs =~
             "fn parse_float_list(value: &Value, path: &str) -> Result<Vec<f64>, String>"

    assert screen_rs =~
             "fn parse_bool_list(value: &Value, path: &str) -> Result<Vec<bool>, String>"

    assert screen_rs =~ "collect::<Result<Vec<i32>, String>>()?"
    assert screen_rs =~ "let model = slint::VecModel::from(parsed);"
  end

  test "projection.codegen emits struct-based id_table bindings" do
    module_name = :"TypedIdTableScreen#{System.unique_integer([:positive])}"
    module = Module.concat([Projection, module_name])

    source = """
    defmodule #{inspect(module)} do
      use ProjectionUI, :screen

      schema do
        field(:devices, :id_table,
          columns: [name: :string, pos: :integer, load: :float, online: :bool],
          default: %{
            order: ["dev-1", "dev-2"],
            by_id: %{
              "dev-1" => %{name: "A", pos: 1, load: 0.5, online: true},
              "dev-2" => %{name: "B", pos: 2, load: 1.0, online: false}
            }
          }
        )
      end

      @impl true
      def render(assigns), do: assigns
    end
    """

    Code.compile_string(source)

    cleanup = setup_codegen_env(router_module: :delete, screen_modules: [module])
    on_exit(cleanup)

    capture_io(fn ->
      Mix.Tasks.Projection.Codegen.run([])
    end)

    screen_name =
      module
      |> Module.split()
      |> List.last()
      |> Macro.underscore()

    # Types file: struct definitions live in per-screen types file
    types_slint =
      File.read!("slint/ui_host/src/generated/#{screen_name}_types.slint")

    assert types_slint =~ "export struct DevicesRow {"
    assert types_slint =~ "id: string,"
    assert types_slint =~ "name: string,"
    assert types_slint =~ "pos: int,"
    assert types_slint =~ "load: float,"
    assert types_slint =~ "online: bool,"

    # State file: imports from per-screen types file (Slint include path, not ui_root-relative)
    state_slint = File.read!("slint/ui_host/src/generated/#{screen_name}_state.slint")
    assert state_slint =~ ~s(import { DevicesRow } from "#{screen_name}_types.slint";)
    assert state_slint =~ "in property <[DevicesRow]> devices: [];"

    refute File.exists?(Path.join(configured_ui_root(), "types.slint")),
           "monolithic types.slint should not be emitted (per-screen types/ instead)"

    # No parallel arrays
    refute state_slint =~ "devices_ids"
    refute state_slint =~ "devices_name"

    # Rust: struct-based construction from parsed id_table
    screen_rs = File.read!("slint/ui_host/src/generated/#{screen_name}.rs")
    assert screen_rs =~ "parse_id_table(value, path)"
    assert screen_rs =~ "crate::DevicesRow {"
    assert screen_rs =~ "id: id.clone().into(),"
    assert screen_rs =~ ~s[parsed.columns.get("name")]
    assert screen_rs =~ ~s[parsed.columns.get("pos")]
    assert screen_rs =~ ~s[parsed.columns.get("load")]
    assert screen_rs =~ ~s[parsed.columns.get("online")]
  end

  test "projection.codegen emits multiple structs for multiple id_tables" do
    module_name = :"MultiIdTableScreen#{System.unique_integer([:positive])}"
    module = Module.concat([Projection, module_name])

    source = """
    defmodule #{inspect(module)} do
      use ProjectionUI, :screen

      schema do
        field(:devices, :id_table,
          columns: [name: :string, status: :string],
          default: %{order: [], by_id: %{}}
        )
        field(:sensors, :id_table,
          columns: [label: :string, value: :float],
          default: %{order: [], by_id: %{}}
        )
      end

      @impl true
      def render(assigns), do: assigns
    end
    """

    Code.compile_string(source)

    cleanup = setup_codegen_env(router_module: :delete, screen_modules: [module])
    on_exit(cleanup)

    capture_io(fn ->
      Mix.Tasks.Projection.Codegen.run([])
    end)

    screen_name =
      module
      |> Module.split()
      |> List.last()
      |> Macro.underscore()

    types_slint =
      File.read!("slint/ui_host/src/generated/#{screen_name}_types.slint")

    assert types_slint =~ "export struct DevicesRow {"
    assert types_slint =~ "export struct SensorsRow {"

    state_slint = File.read!("slint/ui_host/src/generated/#{screen_name}_state.slint")
    assert state_slint =~ ~s(import { DevicesRow, SensorsRow } from "#{screen_name}_types.slint";)
    assert state_slint =~ "in property <[DevicesRow]> devices: [];"
    assert state_slint =~ "in property <[SensorsRow]> sensors: [];"

    screen_rs = File.read!("slint/ui_host/src/generated/#{screen_name}.rs")
    assert screen_rs =~ "crate::DevicesRow {"
    assert screen_rs =~ "crate::SensorsRow {"
  end

