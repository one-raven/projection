  test "compile.projection_codegen retries after failure on next run" do
    module_name = :"RetryScreen#{System.unique_integer([:positive])}"
    module = Module.concat([Projection, module_name])

    source = """
    defmodule #{inspect(module)} do
      use ProjectionUI, :screen

      schema do
        field :data, :map, default: %{}
      end
    end
    """

    Code.compile_string(source)

    cleanup =
      setup_codegen_env(
        router_module: :delete,
        screen_modules: [module],
        restore_codegen: false
      )

    on_exit(cleanup)

    # First run fails due to unsupported :map field
    capture_io(fn ->
      assert {:error, [message]} = Mix.Tasks.Compile.ProjectionCodegen.run([])
      assert message =~ "projection_codegen failed"
    end)

    # Fix the env so next run succeeds
    Application.put_env(:projection, :screen_modules, [Projection.TestScreens.Clock])
    Mix.Task.reenable("projection.codegen")

    capture_io(fn ->
      assert {:ok, []} = Mix.Tasks.Compile.ProjectionCodegen.run([])
    end)
  end

  test "projection.codegen rejects unsupported field types in app_state" do
    module_name = :"BadAppState#{System.unique_integer([:positive])}"
    module = Module.concat([Projection, module_name])

    source = """
    defmodule #{inspect(module)} do
      use ProjectionUI, :app_state

      schema do
        field :data, :map, default: %{}
      end
    end
    """

    Code.compile_string(source)

    original_app_module = Application.get_env(:projection, :app_module)
    Application.put_env(:projection, :app_module, module)

    cleanup = setup_codegen_env(screen_modules: [Projection.TestScreens.Clock])

    on_exit(fn ->
      if is_nil(original_app_module) do
        Application.delete_env(:projection, :app_module)
      else
        Application.put_env(:projection, :app_module, original_app_module)
      end

      cleanup.()
    end)

    assert_raise Mix.Error, ~r/does not support.*:map.*app_state/, fn ->
      capture_io(fn ->
        Mix.Tasks.Projection.Codegen.run([])
      end)
    end
  end

